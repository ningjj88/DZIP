/**
 * java bean 类用来存放field的 各个属性
 */
package csii.base.bean;

/**
 * @author 陈少启
 * @version 1.0.0
 * @since 2010-7-16
 */
public class FieldBean {

	/**
	 * @param args
	 */
	public String fieldName;
	public int fieldlength;
	public String format;
	public boolean fixLength;
	public boolean nullable;

	public FieldBean(String fieldName,boolean fixLength, int fieldlength, String format,	boolean nullable) {
		this.fixLength = fixLength;
		this.fieldlength = fieldlength;
		this.format = format;
		this.nullable = nullable;
		this.fieldName = fieldName;
	}

	public int getFieldlength() {
		return fieldlength;
	}

	public void setFieldlength(int fieldlength) {
		this.fieldlength = fieldlength;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public boolean isFicLength() {
		return fixLength;
	}

	public void setFicLength(boolean fixLength) {
		this.fixLength = fixLength;
	}

	public boolean isNullable() {
		return nullable;
	}

	public void setNullable(boolean nullable) {
		this.nullable = nullable;
	}
}
